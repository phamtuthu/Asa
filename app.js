const express = require("express");
const bodyParser = require("body-parser");
const bitrixRequest = require("./bitrixAuth"); // Import Bitrix API helper

const app = express();
app.use(bodyParser.json()); // H·ªó tr·ª£ JSON body
app.use(express.urlencoded({ extended: true })); // H·ªó tr·ª£ x-www-form-urlencoded

let requestQueue = [];
let isProcessing = false;

// ‚úÖ Ki·ªÉm tra server ho·∫°t ƒë·ªông
app.get("/", (req, res) => {
  res.send("‚úÖ App is running!");
});

// üìå X·ª≠ l√Ω webhook t·ª´ Bitrix24
app.post("/bx24-event-handler", async (req, res) => {
  console.log("üì• Headers:", req.headers);
  console.log("üì• Raw request body:", JSON.stringify(req.body, null, 2));

  if (!req.body || Object.keys(req.body).length === 0) {
    console.error("‚ùå Error: Request body is empty.");
    return res.status(400).json({ error: "Invalid request: Request body is empty." });
  }

  const callData = req.body.data;
  console.log("üìû Extracted callData:", callData);

  if (!callData || !callData.CALL_ID) {
    console.error("‚ùå Error: CALL_ID is missing.");
    return res.status(400).json({ error: "Invalid request: Missing CALL_ID." });
  }

  console.log(`üìû Received call event for CALL_ID: ${callData.CALL_ID}`);
  requestQueue.push({ callData, res });

  if (!isProcessing) {
    processNextRequest();
  }
});

// ‚è≥ X·ª≠ l√Ω t·ª´ng request trong h√†ng ƒë·ª£i
async function processNextRequest() {
  if (requestQueue.length === 0) {
    console.log("‚úÖ All requests processed.");
    isProcessing = false;
    return;
  }

  isProcessing = true;
  const { callData, res } = requestQueue.shift();
  const callId = callData.CALL_ID;

  try {
    // üü¢ L·∫•y th√¥ng tin cu·ªôc g·ªçi t·ª´ Bitrix24
    const callStats = await bitrixRequest(`/voximplant.statistic.get`, "POST", {
      FILTER: { CALL_ID: callId }
    });

    console.log("üìä Bitrix Call Stats:", JSON.stringify(callStats, null, 2));

    if (!callStats?.result?.length) {
      throw new Error("No call data found.");
    }

    const callInfo = callStats.result[0];

    // üïí Chuy·ªÉn ƒë·ªïi th·ªùi gian cu·ªôc g·ªçi
    const callStartDate = convertTimezone(callInfo.CALL_START_DATE, 7);

    const { CRM_ENTITY_ID, CRM_ENTITY_TYPE, CALL_FAILED_REASON, CALL_DURATION } = callInfo;

    if (!CRM_ENTITY_ID) {
      console.error("‚ùå [processNextRequest] CRM_ENTITY_ID is missing. Cannot update.");
      return res.status(400).json({ error: "CRM_ENTITY_ID is missing" });
    }

    // üõ† C·∫≠p nh·∫≠t v√†o Deal ho·∫∑c Contact
    if (CRM_ENTITY_TYPE === "DEAL") {
      await updateDeal(CRM_ENTITY_ID, CALL_FAILED_REASON, CALL_DURATION, callStartDate);
    } else if (CRM_ENTITY_TYPE === "CONTACT") {
      // üîé T√¨m Deal li√™n quan
      const dealData = await bitrixRequest(`/crm.deal.list`, "POST", {
        FILTER: { CONTACT_ID: CRM_ENTITY_ID }
      });

      if (dealData?.result?.length) {
        await updateDeal(dealData.result[0].ID, CALL_FAILED_REASON, CALL_DURATION, callStartDate);
      } else {
        await updateContact(CRM_ENTITY_ID, CALL_DURATION, CALL_FAILED_REASON, callStartDate);
      }
    }

    res.send("‚úÖ Call data processed successfully.");
  } catch (error) {
    console.error("‚ùå Error processing request:", error.message);
    res.status(500).send(error.message);
  }

  processNextRequest();
}

// üîÑ Chuy·ªÉn ƒë·ªïi m√∫i gi·ªù & c·ªông th√™m 1 gi·ªù
function convertTimezone(dateString, targetOffset) {
  const date = new Date(dateString);
  const utc = date.getTime() + date.getTimezoneOffset() * 60000;
  const newDate = new Date(utc + targetOffset * 3600000);
  newDate.setHours(newDate.getHours() + 1); // C·ªông th√™m 1 gi·ªù
  return newDate.toISOString();
}

// üìå C·∫≠p nh·∫≠t Deal trong Bitrix24
async function updateDeal(dealId, callFailedCode, callDuration, callStartDate) {
  const fieldsToUpdate = {
    "UF_CRM_668BB634B111F": callFailedCode,  // Tr·∫°ng th√°i cu·ªôc g·ªçi
    "UF_CRM_66C2B64134A71": callDuration,   // Th·ªùi gian g·ªçi
    "UF_CRM_1733474117": callStartDate,     // Ng√†y g·ªçi
  };

  console.log(`üîÑ [updateDeal] Updating Deal ID: ${dealId}`);
  console.log(`üì§ [updateDeal] Payload:`, JSON.stringify(fieldsToUpdate, null, 2));

  try {
    const response = await bitrixRequest(`/crm.deal.update`, "POST", {
      ID: dealId,
      fields: fieldsToUpdate
    });

    console.log(`‚úÖ [updateDeal] Bitrix Response:`, JSON.stringify(response, null, 2));

    if (response.error) {
      console.error(`‚ùå [updateDeal] Bitrix API error:`, response.error);
    }
  } catch (error) {
    console.error(`‚ùå [updateDeal] Exception:`, error.message);
  }
}

